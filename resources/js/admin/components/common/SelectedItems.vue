<template>
    <table-div
        :heads="heads"
        :data="array_copy"
        :selectable="selectable_copy"
        :link="link"
        :commit="commit"
        :pagination="pagination"
        :clickables="clickables"
        :sortable="sortable"
        :custom_func="custom_func"
    />
</template>
<script>
import TableDiv from "./Table";
export default {
    props:{
        heads:Array,
        data:Array,
        selectable:Object,
        link:String,
        commit:String,
        pagination:Boolean,
        clickables:Boolean,
        sortable:Boolean,
        custom_func:Object,
        changeSelected:Function,
        func:Object
    },
    components: { TableDiv },
    data(){
        return{
            array:JSON.parse(JSON.stringify(this.data)),
            array_copy:JSON.parse(JSON.stringify(this.data)),
            selectable_copy:{},
        }
    },
    watch:{
        array(newVal){
            this.changeSelected(newVal);
        }
    },
    created(){
        this.selectable_copy=copy(this.selectable);

        this.selectable_copy.selected=this.data;
        this.selectable_copy.func=this.func
    }
};
</script>
